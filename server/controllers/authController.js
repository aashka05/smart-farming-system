// @desc    Google OAuth login/register
// @route   POST /api/auth/google
// @access  Public
const googleLogin = async (req, res) => {
  try {
    const { credential } = req.body;
    if (!credential) {
      return res.status(400).json({ message: 'Google credential is required' });
    }

    // Verify the Google ID token
    const ticket = await googleClient.verifyIdToken({
      idToken: credential,
      audience: process.env.GOOGLE_CLIENT_ID,
    });
    const payload = ticket.getPayload();
    const { email, name, sub: googleId } = payload;

    if (!email) {
      return res.status(400).json({ message: 'Could not retrieve email from Google account' });
    }

    // Check if user already exists
    let result = await pool.query('SELECT * FROM farmers WHERE email = $1', [email]);

    if (result.rows.length > 0) {
      // Existing user — log them in
      const user = result.rows[0];
      return res.json({
        id: user.id,
        name: user.full_name,
        email: user.email,
        role: user.role || 'farmer',
        language: 'en',
        token: generateToken(user.id),
      });
    }

    // New user — register with a random password (they'll use Google to login)
    const randomPass = crypto.randomBytes(32).toString('hex');
    const salt = await bcrypt.genSalt(12);
    const hashedPassword = await bcrypt.hash(randomPass, salt);
    const userPhone = `+0${Date.now().toString().slice(-10)}`;

    result = await pool.query(
      `INSERT INTO farmers (full_name, email, phone, password_hash, created_at)
       VALUES ($1, $2, $3, $4, NOW())
       RETURNING *`,
      [name || email.split('@')[0], email, userPhone, hashedPassword]
    );

    const user = result.rows[0];
    res.status(201).json({
      id: user.id,
      name: user.full_name,
      email: user.email,
      role: user.role || 'farmer',
      language: 'en',
      token: generateToken(user.id),
    });
  } catch (error) {
    console.error('Google login error:', error);
    res.status(500).json({ message: 'Google authentication failed', error: error.message });
  }
};

// @desc    Send password reset email
// @route   POST /api/auth/forgot-password
// @access  Public
const forgotPassword = async (req, res) => {
  try {
    const { email } = req.body;
    if (!email) {
      return res.status(400).json({ message: 'Email is required' });
    }

    const result = await pool.query('SELECT id, full_name, email FROM farmers WHERE email = $1', [email]);
    if (result.rows.length === 0) {
      // Don't reveal whether email exists — still say success
      return res.json({ message: 'If an account with that email exists, a reset link has been sent.' });
    }

    const user = result.rows[0];

    // Generate a secure token
    const resetToken = crypto.randomBytes(32).toString('hex');
    const hashedToken = crypto.createHash('sha256').update(resetToken).digest('hex');
    const expires = new Date(Date.now() + 60 * 60 * 1000); // 1 hour

    // Store token in DB
    await pool.query(
      `UPDATE farmers SET reset_token = $1, reset_token_expires = $2 WHERE id = $3`,
      [hashedToken, expires, user.id]
    );

    // Send reset email
    const frontendUrl = process.env.FRONTEND_URL || 'http://localhost:3000';
    const resetUrl = `${frontendUrl}/reset-password/${resetToken}`;

    const transporter = createTransporter();
    await transporter.sendMail({
      from: `"FarmLytics" <${process.env.SMTP_USER}>`,
      to: user.email,
      subject: 'FarmLytics — Password Reset Request',
      html: `
        <div style="font-family: Arial, sans-serif; max-width: 600px; margin: 0 auto; padding: 20px;">
          <div style="background: linear-gradient(135deg, #16a34a, #15803d); padding: 20px; border-radius: 12px 12px 0 0; color: white;">
            <h2 style="margin: 0;">🌾 FarmLytics Password Reset</h2>
          </div>
          <div style="background: #f9fafb; padding: 24px; border: 1px solid #e5e7eb; border-top: none; border-radius: 0 0 12px 12px;">
            <p style="color: #374151;">Hi <strong>${user.full_name}</strong>,</p>
            <p style="color: #4b5563;">You requested a password reset. Click the button below to set a new password:</p>
            <div style="text-align: center; margin: 24px 0;">
              <a href="${resetUrl}" style="background: #16a34a; color: white; padding: 12px 32px; border-radius: 8px; text-decoration: none; font-weight: bold; display: inline-block;">Reset Password</a>
            </div>
            <p style="color: #6b7280; font-size: 14px;">This link will expire in <strong>1 hour</strong>.</p>
            <p style="color: #6b7280; font-size: 14px;">If you didn't request this, you can safely ignore this email.</p>
          </div>
        </div>
      `,
    });

    res.json({ message: 'If an account with that email exists, a reset link has been sent.' });
  } catch (error) {
    console.error('Forgot password error:', error);
    res.status(500).json({ message: 'Failed to send reset email. Please try again.' });
  }
};

// @desc    Reset password with token
// @route   POST /api/auth/reset-password
// @access  Public
const resetPassword = async (req, res) => {
  try {
    const { token, password } = req.body;

    if (!token || !password) {
      return res.status(400).json({ message: 'Token and new password are required' });
    }
    if (password.length < 6) {
      return res.status(400).json({ message: 'Password must be at least 6 characters' });
    }

    // Hash the incoming token to compare with stored hash
    const hashedToken = crypto.createHash('sha256').update(token).digest('hex');

    const result = await pool.query(
      `SELECT id, full_name, email FROM farmers WHERE reset_token = $1 AND reset_token_expires > NOW()`,
      [hashedToken]
    );

    if (result.rows.length === 0) {
      return res.status(400).json({ message: 'Invalid or expired reset token' });
    }

    const user = result.rows[0];

    // Hash new password
    const salt = await bcrypt.genSalt(12);
    const hashedPassword = await bcrypt.hash(password, salt);

    // Update password and clear reset token
    await pool.query(
      `UPDATE farmers SET password_hash = $1, reset_token = NULL, reset_token_expires = NULL WHERE id = $2`,
      [hashedPassword, user.id]
    );

    res.json({ message: 'Password has been reset successfully. You can now login.' });
  } catch (error) {
    console.error('Reset password error:', error);
    res.status(500).json({ message: 'Failed to reset password. Please try again.' });
  }
};

module.exports = { register, login, getMe, googleLogin, forgotPassword, resetPassword };
